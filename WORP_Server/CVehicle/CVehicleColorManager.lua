function CVehicleColorManager:CVehicleColorManager()
	function self.GetRandomColor( iModel )
		local Colors = Colors[ iModel ];
		
		if Colors then
			local Color = Colors[ math.random( table.getn( Colors ) ) ];
			
			local iRed1, iBlue1, iGreen1 =	 unpack( Color[ 1 ] or { 255, 255, 255 } );
			local iRed2, iBlue2, iGreen2 =	 unpack( Color[ 2 ] or { 255, 255, 255 } );
			local iRed3, iBlue3, iGreen3 =	 unpack( Color[ 3 ] or { 255, 255, 255 } );
			local iRed4, iBlue4, iGreen4 =	 unpack( Color[ 4 ] or { 255, 255, 255 } );
			
			return iRed1, iBlue1, iGreen1, iRed2, iBlue2, iGreen2, iRed3, iBlue3, iGreen3, iRed4, iBlue4, iGreen4;
		end
		
		return 255, 255, 255, 0, 0, 0;
	end
