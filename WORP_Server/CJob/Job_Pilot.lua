local OnMarkerHit;

function TJobPilot:Init( pPlayer )
	local pChar = pPlayer:GetChar();
	
	if pChar then
		if pChar:GetLicense( 'aircraft' ) then
			local pVehicle = pPlayer:GetVehicleSeat() == 0 and pPlayer:GetVehicle();
			
			if pVehicle and aSkills[ pVehicle:GetModel() ] then
				if pPlayer:GetChar():GetJobSkill() >= aSkills[ pVehicle:GetModel() ] then
					local pPos		= pPlayer:GetPosition();
					local sCity		= getZoneName( pPos.X, pPos.Y, pPos.Z, true );
					local aCities	= { "Los Santos", "San Fierro", "Las Venturas" };
					
					for i, c in ipairs( aCities ) do
						if c == sCity then
							table.remove( aCities, i );
						end
					end
					
					local sTargetCity	= aCities[ math.random( #aCities ) ];
					local nRoute		= math.random( #aRoutes[ sCity ][ sTargetCity ] );
					
					pPlayer.m_pJob		=
					{
						m_aColor		= { math.random( 0, 255 ); math.random( 0, 255 ); math.random( 0, 255 ); };
						m_aRoutes		= aRoutes[ sCity ][ sTargetCity ][ nRoute ];
						m_nCP			= 1;
					};
					
					self:NextCP( pPlayer );
					
					pPlayer:Hint( "Работа: " + self:GetName(), "Двигайтесь по указанному маршруту", "info" );
				else
					pPlayer:Hint( "Работа: " + self:GetName(), "Ваш навык работы слишком мал чтобы управлять этим транспортным средством", "error" );
				end
			end
		else
			pPlayer:Hint( "Работа: " + self:GetName(), "\nДля этой работы необходима лицензия пилота", "error" );
		end
	end
end

function TJobPilot:Finalize( pPlayer )
	if pPlayer.m_pJob and pPlayer.m_pJob.m_pMarker then
		pPlayer.m_pJob.m_pMarker:Destroy();
		pPlayer.m_pJob.m_pMarker = nil;
	end
end

function TJobPilot:CheckRequirements( pPlayer )
	local pChar = pPlayer:GetChar();
	
	if pChar then
		if pChar:GetLicense( 'aircraft' ) then
			return true;
		else
			pPlayer:Hint( "Работа: " + self:GetName(), "\nДля этой работы необходима лицензия пилота", "error" );
		end
	end
	
	return false;
end

function TJobPilot:NextCP( pPlayer )
	local nCP			= pPlayer.m_pJob.m_nCP;
	local r, g, b		= unpack( pPlayer.m_pJob.m_aColor );
	
	local pMarker		= CMarker.Create( Vector3( unpack( pPlayer.m_pJob.m_aRoutes[ nCP ] ) ), "ring", 20.0, r, g, b, 196, pPlayer.__instance );
	local pBlip			= CBlip( pMarker, 0, 2.0, 0, 255, 255, 255, 0, 9999.0, pPlayer.__instance );
	
	pBlip:SetParent		( pMarker );
	
	pPlayer.m_pJob.m_pMarker	= pMarker;
	
	if pPlayer.m_pJob.m_aRoutes[ nCP + 1 ] then
		local pMarkerNext		= CMarker.Create( Vector3( unpack( pPlayer.m_pJob.m_aRoutes[ nCP + 1 ] ) ), "ring", 20.0, r, g, b, 128, pPlayer.__instance );
		local pBlipNext			= CBlip( pMarkerNext, 0, 1.0, 0, 255, 255, 128, 0, 9999.0, pPlayer.__instance );
		
		pMarker:SetTarget		( pMarkerNext:GetPosition() );
		pMarkerNext:SetTarget	( Vector3( unpack( pPlayer.m_pJob.m_aRoutes[ nCP + 2 ] or pPlayer.m_pJob.m_aRoutes[ nCP ] ) ) );
		pMarkerNext:SetParent	( pMarker );
		pBlipNext:SetParent		( pMarkerNext );
	else
		pMarker:SetTarget		( Vector3( unpack( pPlayer.m_pJob.m_aRoutes[ nCP - 1 ] ) ) );
	end
	
	pMarker.OnHit = OnMarkerHit;
end

function OnMarkerHit( pMarker, pVehicle, bMatching )
	if bMatching and pVehicle:type() == 'vehicle' and pVehicle:GetJob() == TJobPilot then
		local pPlayer = pVehicle:GetDriver();
		
		if pPlayer and pPlayer:IsInGame() then
			local pChar = pPlayer:GetChar();
			
			if pChar and pChar:GetJob() == TJobPilot then
				pPlayer:PlaySoundFrontEnd( 13 );
				
				pPlayer.m_pJob.m_pMarker:Destroy();
				pPlayer.m_pJob.m_pMarker	= nil;
				pPlayer.m_pJob.m_nCP		= pPlayer.m_pJob.m_nCP + 1;
				
				if pPlayer.m_pJob.m_nCP > #pPlayer.m_pJob.m_aRoutes then
					
					local iMoney = math.floor( math.max( 0, ( aPrices[ pVehicle:GetModel() ] or 0 ) * ( pVehicle:GetHealth() * .0015 ) ) );
					
					pChar:SetJobSkill( math.min( 1000, pChar:GetJobSkill() + 1 ) );
					pChar:GivePay( iMoney );
					
					pPlayer:EndCurrentJob();
					
					local iSkill, sTxt = pChar:GetJobSkill(), "";
					
					for m, s in pairs( aSkills ) do
						if s == iSkill then
							sTxt	= "\n\nДоступен новый транспорт";
							
							break;
						end
					end
					
					pPlayer:Hint( "Работа: " + TJobPilot:GetName(), "Вы заработали $" + iMoney + "\nДеньги добавлены к Вашей зарплате\nНавык работы: " + iSkill + sTxt, "info" );
					
					setTimer(
						function()
							pPlayer:InitJob( pChar:GetJob() );
						end,
						5000, 1
					);
					
					return;
				end
				
				return TJobPilot:NextCP( pPlayer );
			end
		end
	end
